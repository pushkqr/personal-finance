package app;

import javax.swing.*;

public class AppUI {
    private JFrame frame;
    private JPanel currentPanel;

    public AppUI() {
        frame = new JFrame("Finance App");
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        frame.setSize(800, 600);
        frame.setLocationRelativeTo(null);

        showLoginScreen();
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void showLoginScreen() {
        if (currentPanel != null) {
            frame.getContentPane().remove(currentPanel);
        }

        currentPanel = new LoginPanel(this);
        frame.getContentPane().add(currentPanel);
        frame.revalidate();
        frame.repaint();
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void showRegistration() {
        if (currentPanel != null) {
            frame.getContentPane().remove(currentPanel);
        }

        currentPanel = new RegistrationPanel(this);
        frame.getContentPane().add(currentPanel);
        frame.revalidate();
        frame.repaint();
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void showDashboard(User user) {
        if (currentPanel != null) {
            frame.getContentPane().remove(currentPanel);
        }

        currentPanel = new DashboardPanel(user);
        frame.getContentPane().add(currentPanel);
        frame.revalidate();
        frame.repaint();
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void start() {
        frame.setVisible(true);
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}
